//1st video
// Function to play the video, hide the button, and show the pop-up with the video
   function video1() {
    const video = document.getElementById('videoPlayer1');
    const playButton = document.getElementById('playButton1');
    const closeButton = document.getElementById('closeButton1');
    const videoModal = document.getElementById('videoModal1');

    // Show the pop-up modal and play the video
    videoModal.style.display = 'flex'; // Show the modal (pop-up)
    video.style.display = 'block'; // Show the video
    video.play(); // Start playing the video

    // Hide the play button
    playButton.style.display = 'none';

    // Show the close button inside the modal
    closeButton.style.display = 'block';
}

// Function to close (hide) the video modal and show the play button again
function closeVideo1() {
    const video = document.getElementById('videoPlayer1');
    const playButton = document.getElementById('playButton1');
    const closeButton = document.getElementById('closeButton1');
    const videoModal = document.getElementById('videoModal1');

    // Pause the video and hide it
    video.pause();
    video.style.display = 'none';

    // Show the play button again
    playButton.style.display = 'block';

    // Hide the close button
    closeButton.style.display = 'none';

    // Hide the video modal (pop-up)
    videoModal.style.display = 'none';
}

